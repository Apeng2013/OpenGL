unsigned int TextureFromFile(const char* path, const std::string& directory)
{
	unsigned int texture_id;
	glGenTextures(1, &texture_id);

	int width, height, bpp;
	std::string file_path = directory + "/" + std::string(path);
	unsigned char* local_buffer = stbi_load(file_path.c_str(), &width, &height, &bpp, 4);
	if (local_buffer)
	{
		glBindTexture(GL_TEXTURE_2D, texture_id);

		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
		glTexParameteri(GL_TEXTURE_2D, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);

		glTexImage2D(GL_TEXTURE_2D, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, local_buffer);
		stbi_image_free(local_buffer);
	}
	else
	{
		std::cout << "Load Texture ERROR: " << file_path << std::endl;
	}

	return texture_id;
}

Model::Model(const std::string& path)
{
	LoadModel(path);
}

